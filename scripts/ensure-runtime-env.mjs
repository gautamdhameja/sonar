import { randomBytes } from "node:crypto";
import { chmodSync, existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";

const runtimeEnvPath = join(process.cwd(), ".sonar", "runtime.env");

function parseEnvFile(path) {
  if (!existsSync(path)) return new Map();
  const values = new Map();
  for (const line of readFileSync(path, "utf8").split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const separator = trimmed.indexOf("=");
    if (separator === -1) continue;
    values.set(trimmed.slice(0, separator).trim(), trimmed.slice(separator + 1).trim());
  }
  return values;
}

function token() {
  return randomBytes(32).toString("hex");
}

const values = parseEnvFile(runtimeEnvPath);
const apiToken = process.env.SONAR_API_TOKEN || values.get("SONAR_API_TOKEN") || token();
const meiliMasterKey =
  process.env.SONAR_MEILI_MASTER_KEY || values.get("SONAR_MEILI_MASTER_KEY") || apiToken;

mkdirSync(dirname(runtimeEnvPath), { recursive: true });
writeFileSync(
  runtimeEnvPath,
  [
    "# Generated by Sonar. Do not commit.",
    `SONAR_API_TOKEN=${apiToken}`,
    `SONAR_MEILI_MASTER_KEY=${meiliMasterKey}`,
    `SONAR_MEILI_API_KEY=${meiliMasterKey}`,
    `MEILI_MASTER_KEY=${meiliMasterKey}`,
    "",
  ].join("\n"),
);
chmodSync(runtimeEnvPath, 0o600);
console.log(`Runtime env ready: ${runtimeEnvPath}`);
